        private void ReturnKeyClick(object sender, KeyRoutedEventArgs e)
        {
            if (e.Key == Windows.System.VirtualKey.Enter)
            {
                lblResultado.Text = txtDigita.Text;
                Windows.UI.ViewManagement.InputPane.GetForCurrentView().TryHide();
            }
        }
